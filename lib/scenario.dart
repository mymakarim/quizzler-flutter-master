class Scenario {
  int scenarioID;
  String scenarioName;
  String scenarioDesc;
  String scenarioImage;
  String scenarioImageBig;

  Scenario({int sID, String name, String desc, String image, String imageBig}) {
    scenarioID = sID;
    scenarioName = name;
    scenarioDesc = desc;
    scenarioImage = image;
    scenarioImageBig = imageBig;
  }
}
